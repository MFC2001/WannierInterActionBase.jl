struct RealKeldyshGauss2D <: RealCoulomb
	r₀::Float64
	ϵ₁::Float64
	ϵ₂::Float64
	α::Float64
	CoulombScaleϵ::Float64
	V₀::Float64
	α²4::Float64
end
function (v::RealKeldyshGauss2D)(r::AbstractVector{<:Real}; atol::Real = 1e-10)::Float64
	r = norm(r)
	return r ≤ atol ? v.V₀ : _RealKeldyshGauss2D(v, r)
end
function (v::RealKeldyshGauss2D)(r::Real; atol::Real = 1e-10)::Float64
	signbit(r) && error("r should be positive")
	return r ≤ atol ? v.V₀ : _RealKeldyshGauss2D(v, r)
end
function _RealKeldyshGauss2D(v, r)::Float64
	t₁ = 1 / (v.α²4 * r * r)
	t₂ = r / v.r₀
	integrand(x) = besselj0(x) * exp(-x * x * t₁) / (t₂ + x)
	result, err = quadgk(integrand, 0.0, Inf; rtol = 1e-8, atol = 1e-10)
	return v.CoulombScaleϵ * result
end
