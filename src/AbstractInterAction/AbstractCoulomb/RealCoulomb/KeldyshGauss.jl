abstract type RealCoulombKeldyshGauss <: RealCoulomb end
struct RealCoulombKeldyshGaussn0 <: RealCoulombKeldyshGauss
	r₀::Float64
	ϵ₁::Float64
	ϵ₂::Float64
	α::Float64
	αz::Float64
	coff::Float64
	V₀::Float64
	α²4::Float64
	αz2::Float64
end
function (v::RealCoulombKeldyshGaussn0)(r::AbstractVector{<:Real}; atol::Real = 1e-10)::Float64
	r = norm(r)
	return r ≤ atol ? v.V₀ : _RealCoulombKeldyshGaussn0(v, r)
end
function (v::RealCoulombKeldyshGaussn0)(r::Real; atol::Real = 1e-10)::Float64
	signbit(r) && error("r should be positive")
	return r ≤ atol ? v.V₀ : _RealCoulombKeldyshGaussn0(v, r)
end
function _RealCoulombKeldyshGaussn0(v, r)::Float64
	t₁ = 1 / (v.α²4 * r * r)
	t₂ = 1 / (v.αz2 * r)
	t₃ = r / v.r₀
	integrand(x) = besselj0(x) * exp(- t₁ * x * x) * erfcx(t₂ * x) / (t₃ + x)
	result, err = quadgk(integrand, 0.0, Inf; rtol = 1e-8, atol = 1e-10)
	return v.coff * result
end
